public class Aufgabe3_2 { //wurde in Orientierung gemacht
    public static void main(String[] args) {

        int a;
        int b;

        for (a = 1; a <= 10; a++) {
            for (b = 1; b <= 10; b++) {

                System.out.println(a + "x" + b + "=" + a * b);
            }
        }


    }
}








